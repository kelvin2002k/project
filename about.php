Nike has faced controversy over the years, particularly in relation to labor practices in its overseas factories, as well as its marketing campaigns and athlete sponsorships. However, the company has also been recognized for its commitment to sustainability and social responsibility, including efforts to reduce waste and carbon emissions and to promote diversity and inclusion in its workforce.


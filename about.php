      Nike, Inc. is an American multinational corporation that designs, develops, and sells athletic footwear, apparel, equipment, and accessories. The company is headquartered near Beaverton, Oregon, in the Portland metropolitan area. It was founded on January 25, 1964, by Bill Bowerman and Phil Knight, and originally known as Blue Ribbon Sports. The company was renamed Nike, Inc. on May 30, 1971.

